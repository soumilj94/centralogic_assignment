class CarouselItem {
  final int id;
  final String name;
  final String description;
  final double price;
  final int rating;
  final String availability;
  final String vendor_image;
  final String image;

  CarouselItem({
    required this.id,
    required this.name,
    required this.description,
    required this.price,
    required this.rating,
    required this.availability,
    required this.vendor_image,
    required this.image,
  });
}